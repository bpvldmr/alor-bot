last_balance      = None
total_profit      = 0
total_deposit     = 0
total_withdrawal  = 0

SIGNAL_COOLDOWN_SECONDS = 3600      # 1-—á–∞—Å–æ–≤–æ–π cooldown –¥–ª—è RSI
# ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

def get_alor_symbol(instrument: str) -> str:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –±–∏—Ä–∂–µ–≤–æ–π symbol –¥–ª—è ALOR-API.
       –°–µ–π—á–∞—Å trade == symbol, –Ω–æ –æ—Å—Ç–∞–≤–ª—è–µ–º –≥–∏–±–∫–æ—Å—Ç—å –Ω–∞ –±—É–¥—É—â–µ–µ."""
    for info in TICKER_MAP.values():
        if info["trade"] == instrument:
            return info["symbol"]
    return instrument      # fallback

async def get_account_summary():
    token = await get_access_token()
    url   = f"{BASE_URL}/md/v2/Clients/MOEX/{ACCOUNT_ID}/summary"
    headers = {"Authorization": f"Bearer {token}", "Accept": "application/json"}
    async with httpx.AsyncClient(timeout=10) as client:
        r = await client.get(url, headers=headers)
        r.raise_for_status()
        return r.json()

async def execute_market_order(symbol: str, side: str, qty: int):
    """–û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —Ä—ã–Ω–æ–∫ –∏ —á–µ—Ä–µ–∑ 30 —Å –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ—Ç —Ñ–∞–∫—Ç–∏—á–µ—Å–∫—É—é –ø–æ–∑–∏—Ü–∏—é."""
    res = await place_order({
        "side": side.upper(),
        "qty":  qty,
        "instrument": symbol,
        "symbol":     symbol,
    })

    print("üìÖ Order sent, got response:", res)

    if "error" in res:
        await send_telegram_log(f"‚ùå {side}/{symbol}/{qty}: {res['error']}")
        return None

    await asyncio.sleep(30)
    snapshot = await get_position_snapshot(symbol)
    actual_position = snapshot.get("qty", 0)

    return {
        "price":     res.get("price", 0.0),
        "order_id":  res.get("order_id", "‚Äî"),
        "position":  actual_position
    }

# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê  –û–°–ù–û–í–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø  ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
async def process_signal(tv_tkr: str, sig: str):
    global total_profit, initial_balance, last_balance, total_deposit, total_withdrawal

    await send_telegram_log(f"üìÖ –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–∏–≥–Ω–∞–ª–∞: {tv_tkr} / {sig}")

    if tv_tkr not in TICKER_MAP:
        await send_telegram_log(f"‚ö†Ô∏è –ù–µ–∏–∑–≤–µ—Å—Ç–Ω—ã–π —Ç–∏–∫–µ—Ä {tv_tkr}")
        return {"error": "Unknown ticker"}

    symbol    = TICKER_MAP[tv_tkr]["trade"]      # trade == symbol
    sig_upper = sig.upper()

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ RSI > 70 / RSI < 30 ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    if sig_upper in ("RSI>70", "RSI<30"):
        now = time.time()
        key = f"{symbol}:{sig_upper}"
        if last_signals.get(key) and now - last_signals[key] < SIGNAL_COOLDOWN_SECONDS:
            await send_telegram_log(f"‚è≥ –°–∏–≥–Ω–∞–ª –ø—Ä–æ–∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞–Ω (cooldown): {tv_tkr}/{sig}")
            return {"status": "ignored"}
        last_signals[key] = now

        positions = await get_current_positions()
        cur = positions.get(symbol, 0)
        current_positions[symbol] = cur

        if cur == 0:
            await send_telegram_log(f"‚ö†Ô∏è –ù–µ—Ç –æ—Ç–∫—Ä—ã—Ç–æ–π –ø–æ–∑–∏—Ü–∏–∏ –ø–æ {symbol}, —Å–∏–≥–Ω–∞–ª {sig} –ø—Ä–æ–∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞–Ω")
            return {"status": "no_position"}

        if sig_upper == "RSI>70":
            if cur > 0:
                half = abs(cur) // 2
                if half:
                    res = await execute_market_order(symbol, "sell", half)
                    if res:
                        current_positions[symbol] = cur - half
                        await send_telegram_log(
                            f"üìâ RSI>70: –ü—Ä–æ–¥–∞—ë–º –ø–æ–ª–æ–≤–∏–Ω—É LONG –ø–æ {symbol}\n–ö–æ–Ω—Ç—Ä–∞–∫—Ç—ã: {half}\n–¶–µ–Ω–∞: {res['price']:.2f}"
                        )
                return {"status": "partial_long_close"}
            await send_telegram_log(f"‚ö†Ô∏è RSI>70: –£ –≤–∞—Å SHORT –ø–æ {symbol}, –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º")
            return {"status": "noop"}

        if sig_upper == "RSI<30":
            if cur < 0:
                half = abs(cur) // 2
                if half:
                    res = await execute_market_order(symbol, "buy", half)
                    if res:
                        current_positions[symbol] = cur + half
                        await send_telegram_log(
                            f"üìà RSI<30: –ü–æ–∫—É–ø–∞–µ–º –ø–æ–ª–æ–≤–∏–Ω—É SHORT –ø–æ {symbol}\n–ö–æ–Ω—Ç—Ä–∞–∫—Ç—ã: {half}\n–¶–µ–Ω–∞: {res['price']:.2f}"
                        )
                return {"status": "partial_short_close"}
            await send_telegram_log(f"‚ö†Ô∏è RSI<30: –£ –≤–∞—Å LONG –ø–æ {symbol}, –Ω–∏—á–µ–≥–æ –Ω–µ –¥–µ–ª–∞–µ–º")
            return {"status": "noop"}

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ LONG0 / SHORT0  (–ø–æ–ª–Ω–æ–µ –∑–∞–∫—Ä—ã—Ç–∏–µ) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    if sig_upper in ("LONG0", "SHORT0"):
        positions = await get_current_positions()
        cur = positions.get(symbol, 0)
        current_positions[symbol] = cur

        if cur == 0:
            await send_telegram_log(f"‚ö†Ô∏è –ù–µ—Ç –ø–æ–∑–∏—Ü–∏–∏ –ø–æ {symbol}, —Å–∏–≥–Ω–∞–ª {sig_upper} –ø—Ä–æ–∏–≥–Ω–æ—Ä–∏—Ä–æ–≤–∞–Ω")
            return {"status": "no_position"}

        # LONG0 –∑–∞–∫—Ä—ã–≤–∞–µ—Ç SHORT, SHORT0 –∑–∞–∫—Ä—ã–≤–∞–µ—Ç LONG
        need_close_short = sig_upper == "LONG0"  and cur < 0
        need_close_long  = sig_upper == "SHORT0" and cur > 0
        if not (need_close_short or need_close_long):
            await send_telegram_log(
                f"‚ö†Ô∏è –°–∏–≥–Ω–∞–ª {sig_upper} –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ–º –ø–æ–∑–∏—Ü–∏–∏ {cur:+}"
            )
            return {"status": "direction_mismatch"}

        side = "buy" if cur < 0 else "sell"
        qty  = abs(cur)

        res = await execute_market_order(symbol, side, qty)
        if res:
            current_positions[symbol] = 0
            entry_prices.pop(symbol, None)
            await send_telegram_log(
                f"üîí {sig_upper}: –ø–æ–∑–∏—Ü–∏—è –ø–æ {symbol} –∑–∞–∫—Ä—ã—Ç–∞ –ø–æ–ª–Ω–æ—Å—Ç—å—é\n"
                f"–ö–æ–Ω—Ç—Ä–∞–∫—Ç—ã: {qty}\n–¶–µ–Ω–∞: {res['price']:.2f}"
            )
            summary = await get_account_summary()
            await send_balance_to_telegram(summary, total_profit, initial_balance or 1)
        return {"status": "close_all"}

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ –û–±—ã—á–Ω—ã–µ LONG / SHORT ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    dir_  = 1 if sig_upper == "LONG" else -1
    side  = "buy" if dir_ > 0 else "sell"

    positions = await get_current_positions()
    cur = positions.get(symbol, 0)
    current_positions[symbol] = cur

    # 1‚É£ –ü–µ—Ä–µ–≤–æ—Ä–æ—Ç ------------------------------------------------------------
    if cur * dir_ < 0:
        await send_telegram_log(f"üîÑ –ü–µ—Ä–µ–≤–æ—Ä–æ—Ç: {symbol}, —Å–∏–≥–Ω–∞–ª {sig_upper}, –ø–æ–∑–∏—Ü–∏—è {cur}")
        total_qty = abs(cur) + START_QTY[symbol]
        res = await execute_market_order(symbol, side, total_qty)
        if res:
            price = res["price"]
            actual_position = res["position"]
            prev_entry = entry_prices.get(symbol, 0)
            pnl = (price - prev_entry) * cur
            pct = (pnl / (abs(prev_entry) * abs(cur)) * 100) if prev_entry else 0

            current_balance = await get_current_balance()
            if initial_balance is None:
                initial_balance = current_balance
                last_balance    = current_balance

            theoretical_balance = last_balance + pnl
            diff = round(current_balance - theoretical_balance, 2)
            if diff > 10:
                total_deposit += diff
            elif diff < -10:
                total_withdrawal += abs(diff)

            last_balance = current_balance
            total_profit += pnl

            await log_trade_result(
                ticker=symbol,
                side="LONG" if cur > 0 else "SHORT",
                qty=cur,
                entry_price=prev_entry,
                exit_price=price
            )

            if actual_position * dir_ > 0:   # –ø–µ—Ä–µ–≤–æ—Ä–æ—Ç —É–¥–∞–ª—Å—è
                current_positions[symbol] = actual_position
                entry_prices[symbol]      = price
                emoji = "üîª" if pnl < 0 else "üü¢"
                await send_telegram_log(
                    f"{emoji} –ü–µ—Ä–µ–≤–æ—Ä–æ—Ç –∑–∞–≤–µ—Ä—à—ë–Ω:\n"
                    f"–¢–∏–∫–µ—Ä: {symbol}\n"
                    f"–î–µ–π—Å—Ç–≤–∏–µ: {'LONG' if cur > 0 else 'SHORT'} ‚Üí "
                    f"{'LONG' if dir_ > 0 else 'SHORT'}\n"
                    f"–ö–æ–Ω—Ç—Ä–∞–∫—Ç—ã: {abs(total_qty)}\n"
                    f"–í—Ö–æ–¥: {prev_entry:.2f} ‚Üí –í—ã—Ö–æ–¥: {price:.2f}\n"
                    f"PnL: {pnl:+.2f} —Ä—É–±. ({pct:+.2f}%)\n"
                    f"–ë–∞–ª–∞–Ω—Å: {current_balance:.2f} —Ä—É–±."
                )
            else:
                await send_telegram_log(
                    f"‚ö†Ô∏è –ü–µ—Ä–µ–≤–æ—Ä–æ—Ç –Ω–µ –∑–∞–≤–µ—Ä—à—ë–Ω! –ó–∞–ø—Ä–æ—à–µ–Ω–æ {total_qty}, –ø–æ–∑–∏—Ü–∏—è: {actual_position:+}"
                )

            summary = await get_account_summary()
            await send_balance_to_telegram(summary, total_profit, initial_balance or 1)

        return {"status": "flip"}

    # 2‚É£ –£—Å—Ä–µ–¥–Ω–µ–Ω–∏–µ -----------------------------------------------------------
    if cur * dir_ > 0:
        new = cur + ADD_QTY[symbol]
        if abs(new) > MAX_QTY[symbol]:
            await send_telegram_log(f"‚ùå –ü—Ä–µ–≤—ã—à–µ–Ω –ª–∏–º–∏—Ç –ø–æ {symbol}: {MAX_QTY[symbol]}")
            return {"status": "limit"}

        res = await execute_market_order(symbol, side, ADD_QTY[symbol])
        if res:
            price = res["price"]
            entry_prices[symbol] = (
                (entry_prices.get(symbol, 0) * abs(cur) + price * ADD_QTY[symbol]) / abs(new)
            )
            current_positions[symbol] = new
            await send_telegram_log(
                f"‚ûï –£—Å—Ä–µ–¥–Ω–µ–Ω–∏–µ {symbol}={new:+} @ {entry_prices[symbol]:.2f}"
            )

            summary = await get_account_summary()
            await send_balance_to_telegram(summary, total_profit, initial_balance or 1)

        return {"status": "avg"}

    # 3‚É£ –û—Ç–∫—Ä—ã—Ç–∏–µ -------------------------------------------------------------
    if cur == 0:
        res = await execute_market_order(symbol, side, START_QTY[symbol])
        if res:
            price = res["price"]
            current_positions[symbol] = dir_ * START_QTY[symbol]
            entry_prices[symbol]      = price
            await send_telegram_log(
                f"‚úÖ –û—Ç–∫—Ä—ã—Ç–∞ {symbol}={dir_ * START_QTY[symbol]:+} @ {price:.2f}"
            )

            summary = await get_account_summary()
            await send_balance_to_telegram(summary, total_profit, initial_balance or 1)

        return {"status": "open"}

    return {"status": "noop"}

